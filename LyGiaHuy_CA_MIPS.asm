############		Win condition for players		############
# $s0 now have the address of the checking position
# $s3 will be loaded the address of $s0 to be modified
# $s4 will be the win condition, in the begin, s4 = 4
# $t0 represent the player piece
# $t1 represent the UUT (Unit under test)
# $t2 is the counter for number of loop
	WinConditionForPlayer:
			add $s3, $s0, $zero				# $s3 have the address of the checking position
			li $s4, 4					# $s4 is the counter for win-condition
			lw $t0, player1
			lw $t1, ($s3)					# $t0 have the player's piece
			beq $t0, $t1, PlayerPick
			lw $t0, player2
			PlayerPick: la $t0, playercheck
				    sw $t1, ($t0)
		  	j checkingVertical
		
		checkingVertical:
			add $s3, $s0, $zero				# $s3 have the address of the checking position
			li $s4, 4					# $s4 is the counter for win-condition
			li $t2, 4					# $t2 is the counter for loop
			checkingVertical_loop:				
				lw $t0, playercheck			# $t0 have player1 piece
				lw $t1, ($s3)				# $t1 have the UUT piece
				bne $t1, $t0, checkingHorizontal	# if $t0 != $t1 => check next stage 
				#if their equal, we update counters			
				add $s3, $s3, Row_byte			# jump to next ROW	
				#Win condition when $s4 = 0	
				sub $s4, $s4, 1
				beqz $s4, win	
				############################
				sub $t2, $t2, 1
				beq $t2, $zero, checkingHorizontal	
			j checkingVertical_loop
			
			checkingHorizontal:
			la $s2, board					# $s2 will have address of the first char
			add $s3, $s0, $zero				# $s3 have the address of the checking position
			li $s4, 4					# $s4 is the counter for win-condition
			li $t2, 4					# $t2 is the counter for loop
			MoveLeftLoop:
				sub $s3, $s3, 4
				beq $s3, $s2, checkingHorizontal_Start1
				sub $s3, $s3, 4
				lw $t0, playercheck			# $t0 have player1 piece
				lw $t1, ($s3)				# $t1 have the UUT piece
				bne $t1, $t0, checkingHorizontal_Start	# if $t0 != $t1 => check next stage
				sub $s4, $s4, 1
				beqz $s4, win
				sub $t2, $t2, 1
				beq $t2, $zero, checkingHorizontal_Start
				j MoveLeftLoop
			checkingHorizontal_Start:
				add $s3, $s3, 4
			checkingHorizontal_Start1:
				add $s3, $s3, 4
				li $s4, 4					# $s4 is the counter for win-condition
				li $t2, 4					# $t2 is the counter for loop
			checkingHorizontal_Loop:
				lw $t0, playercheck			# $t0 have player1 piece
				lw $t1, ($s3)				# $t1 have the UUT piece
				bne $t1, $t0, checkFirstDiagonal	# if $t0 != $t1 => check next stage 
				#if their equal, we update counters			
				add $s3, $s3, 8				# jump to next Col	
				#Win condition when $s4 = 0	
				sub $s4, $s4, 1
				beqz $s4, win	
				############################
				sub $t2, $t2, 1
				beq $t2, $zero, checkFirstDiagonal	
			j checkingHorizontal_Loop
			
			checkFirstDiagonal:
				la $s2, board					# $s2 will have address of the first char
				add $s2, $s2, Row_byte				# $s2 represent the edge of first row
				add $s3, $s0, $zero				# $s3 have the address of the checking position
				li $s4, 4					# $s4 is the counter for win-condition
				li $t2, 4					# $t2 is the counter for loop			
			MoveCrossFirstDiagonalLoop:
				blt $s3, $s2, checkingFirstDiagonal_Start1	# if the checking position is at the first row
				sub $s3, $s3, firstCross			# Move to the left-up cross
				
				lw $t0, playercheck				# $t0 have player1 piece
				lw $t1, ($s3)					# $t1 have the UUT piece
				bne $t1, $t0, checkingFirstDiagonal_Start		# if $t0 != $t1 => check next stage
				sub $s4, $s4, 1
				beqz $s4, win
				sub $t2, $t2, 1
				beq $t2, $zero, checkingFirstDiagonal_Start
				j MoveCrossFirstDiagonalLoop
			checkingFirstDiagonal_Start:
				add $s3,$s3, firstCross				# $t2 is the counter for loop				
			checkingFirstDiagonal_Start1:
				li $s4, 4					# $s4 is the counter for win-condition
				li $t2, 4
			checkingFirstDiagonal_Loop:
				lw $t0, playercheck			# $t0 have player1 piece
				lw $t1, ($s3)				# $t1 have the UUT piece
				bne $t1, $t0, checkSecondDiagonal	# if $t0 != $t1 => check next stage 
				#if their equal, we update counters			
				add $s3, $s3, firstCross		# jump to next cross	
